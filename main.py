@app.route('/register', methods=['POST', 'GET'])
def register():
    register_form = CreateRegisterForm()

    if register_form.validate_on_submit():
        hashed_password = generate_password_hash(password=register_form.password.data,
                                                 method="pbkdf2:sha256",
                                                 salt_length=8)
        new_user = User(
            name=register_form.name.data,
            email=register_form.email.data,
            password=hashed_password
        )
        db.session.add(new_user)
        db.session.commit()
        login_user(user=new_user)
        return redirect(url_for('get_all_posts'))

    return render_template("register.html", form=register_form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=['GET', 'POST'])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit() and request.method == 'POST':
        email = request.form.get("email")
        password = request.form.get("password")
        user = User.query.filter_by(email=email).first()
        if not user:
            flash(message="Please check your email you have entered.")
            return redirect(url_for('login'))
        elif not check_password_hash(password=password, pwhash=user.password):
            flash(message="Password Incorrect. Please check the password and try again")
            return redirect(url_for('login'))
        else:
            login_user(user=user)
            return redirect(url_for('get_all_posts'))
    return render_template("login.html", form=login_form, logged_in=current_user.is_authenticated)


